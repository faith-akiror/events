// Helper to generate a unique ID
function uuid() {
    return 'xxxxxxxx-xxxx-4xxx-yxxx-xxxxxxxxxxxx'.replace(/[xy]/g, function(c) {
        const r = Math.random() * 16 | 0, v = c === 'x' ? r : (r & 0x3 | 0x8);
        return v.toString(16);
    });
}

// Get all events from localStorage
async function getEvents() {
    const snapshot = await db.collection('events').get();
    const events = [];
    snapshot.forEach(doc => {
        events.push({ id: doc.id, ...doc.data() });
    });
    return events;
}

// Save all events to localStorage
async function createEvent(eventData) {
    // Ensure a description field exists, even if empty, to match the data model.
    const completeEventData = { ...eventData, description: eventData.description || '' };

    // The 'id' will be auto-generated by Firestore's add() method.
    const docRef = await db.collection('events').add(completeEventData);
    return docRef.id;
}

// Update an existing event in Firestore
async function updateEvent(eventId, eventData) {
    const eventRef = db.collection('events').doc(eventId);
    await eventRef.update(eventData);
}

// Get a single event by its ID
async function getEventById(eventId) {
    const doc = await db.collection('events').doc(eventId).get();
    return doc.exists ? { id: doc.id, ...doc.data() } : null;
}

