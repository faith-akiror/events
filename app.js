// Save a new attendee/ticket.
async function saveAttendee(eventId, newAttendee) {
    // The newAttendee object already contains the ticket ID.
    // We will use that as the document ID in Firestore.
    const ticketRef = db.collection('tickets').doc(newAttendee.id);
    await ticketRef.set({ ...newAttendee, eventId: eventId });
}

// Function to clear all registered tickets from Firestore
async function deleteAllTickets() {
    if (confirm('Are you sure you want to delete ALL ticket data? This cannot be undone.')) {
        const snapshot = await db.collection('tickets').get();
        const batch = db.batch();
        snapshot.docs.forEach(doc => {
            batch.delete(doc.ref);
        });
        await batch.commit();
        alert('All ticket data has been deleted.');
    }
}

// --- ONE-TIME DATA SEEDING ---
// This function uploads the initial data from events-data.js to Firestore.
// It should only be run once to avoid creating duplicate data.
// It uses .set() so it will overwrite existing data with the same ID.
async function seedInitialDataToFirestore() {
    if (!confirm("Are you sure you want to seed the initial data? This will upload all events and tickets from events-data.js, overwriting any existing data with the same IDs.")) {
        return;
    }

    console.log("Starting to seed data...");
    const { events, tickets } = getInitialData();
    const batch = db.batch();

    // Add events to the batch. Using the event.id as the document ID.
    events.forEach(event => {
        const eventRef = db.collection('events').doc(event.id);
        batch.set(eventRef, event);
    });

    // Add tickets to the batch. Using the ticket.id as the document ID.
    tickets.forEach(ticket => {
        const ticketRef = db.collection('tickets').doc(ticket.id);
        batch.set(ticketRef, ticket);
    });

    // Commit the batch
    await batch.commit();
    console.log("Successfully seeded events and tickets to Firestore!");
    alert("Initial events and tickets have been uploaded to the database. Please refresh the page.");
}